package kr.happyjob.study.adm.model;

public class EmployModel {
	
	private String loginID;
	private String name;
	private String user_hp;
	private String user_email;
	private String employ_join;
	private int employ_no;
	private String employ_leave;
	private String employ_name;
	private String employ_state;
	private String user_regdate;
	
	
	public String getLoginID() {
		return loginID;
	}
	public void setLoginID(String loginID) {
		this.loginID = loginID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUser_hp() {
		return user_hp;
	}
	public void setUser_hp(String user_hp) {
		this.user_hp = user_hp;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	public String getEmploy_join() {
		return employ_join;
	}
	public void setEmploy_join(String employ_join) {
		this.employ_join = employ_join;
	}
	public int getEmploy_no() {
		return employ_no;
	}
	public void setEmploy_no(int employ_no) {
		this.employ_no = employ_no;
	}
	public String getEmploy_leave() {
		return employ_leave;
	}
	public void setEmploy_leave(String employ_leave) {
		this.employ_leave = employ_leave;
	}
	public String getEmploy_name() {
		return employ_name;
	}
	public void setEmploy_name(String employ_name) {
		this.employ_name = employ_name;
	}
	public String getUser_regdate() {
		return user_regdate;
	}
	public void setUser_regdate(String user_regdate) {
		this.user_regdate = user_regdate;
	}
	public String getEmploy_state() {
		return employ_state;
	}
	public void setEmploy_state(String employ_state) {
		this.employ_state = employ_state;
	}
	
}
